package com.dradest.sample.generatejavadocs;

/**
 * Sample class with an int field and String field.
 * Getter and setter methods for those fields.
 */
public class SampleClass {
	/**
	 * Field x of type int
	 */
	int x;
	/**
	 * Field str of type String
	 */
	String str;
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}
	/**
	 * @return the str
	 */
	public String getStr() {
		return str;
	}
	/**
	 * @param str the str to set
	 */
	public void setStr(String str) {
		this.str = str;
	}
	
}
